import mongoose from "mongoose";

const TeamsSchema = new mongoose.Schema({
  name: String, 
  description: String,
  company: String,
  createdOn: {
    type: Date,
    default: Date.now,
  },
  createdBy: {
    type: String,
    default: "",
  },
  deleted: {
    type: Boolean,
    default: 0,
  },
  deletedBy: {
    type: String,
    default: "",
  },
  deletedOn: {
    type: Date,
    default: "",
  },
  modifiedBy: {
    type: String,
    default: "",
  },
  modifiedOn: {
    type: Date,
    default: "",
  },
});

const Teams = mongoose.model("Teams", TeamsSchema);
export default Teams;
